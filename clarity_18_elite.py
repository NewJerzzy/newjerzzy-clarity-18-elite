def check_scan_timing(sport: str) -> None:
    now = datetime.now()
    hour = now.hour
    weekday = now.weekday()
    if sport in ["NBA", "MLB", "NHL"]:
        if hour not in [6, 14, 21]:
            st.warning("⏰ Optimal scanning times for NBA/MLB/NHL are 6 AM, 2 PM, and 9 PM. Current time may yield less stable lines.")
